    public void sendVerificationEmail(String to, String token) {

        log.info("Sending verification email to {}", to);

        String domainName = fromEmail.substring(fromEmail.lastIndexOf("@"));
        String noReply = "MySpringBootApp <noreply" + domainName + ">";
        String url = getBaseUrl() + "/api/v1/auth/verify?token=" + token;

        MimeMessage mimeMessage = mailSender.createMimeMessage();
        try {
            MimeMessageHelper helper = new MimeMessageHelper(mimeMessage, true);
            helper.setText("", buildBody(to, token, url));
            helper.setFrom(noReply);
            helper.setTo(to);
            helper.setSubject("Verify Your Account at MySpringBootApp");
            helper.setReplyTo(noReply);
            mailSender.send(mimeMessage);
        } catch (MessagingException e) {
            throw new MailSendException("Mail sending operation failed: " + e.getMessage());
        }
        log.info("Successfully sent verification email to {}", to);
    }

